package com.company;

public class Fish {
    private String type;
    private String color;
    private int weight;

    public void setType(String fishType){
        type=fishType;
    }
    public String getType(){
        return type;
    }
    public void setColor(String fishColor){
        color=fishColor;
    }
    public String getColor(){
        return color;
    }
    public void setWeight(int fishWeight){
        weight=fishWeight;
    }
    public int getWeight(){
        return weight;
    }

}
