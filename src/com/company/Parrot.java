package com.company;

public class Parrot {
    private String type;
    private String color;
    private int weight;

    public void setType(String parrotType){
        type=parrotType;
    }
    public String getType(){
        return type;
    }
    public void setColor(String parrotColor){
        color=parrotColor;
    }
    public String getColor(){
        return color;
    }
    public void setWeight(int parrotWeight){
        weight=parrotWeight;
    }
    public int getWeight(){
        return weight;
    }

}
